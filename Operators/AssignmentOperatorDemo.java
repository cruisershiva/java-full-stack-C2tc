//Program to demonstrate the Assignment operator
package org.tnsif.operators;

public class AssignmentOperatorDemo {

	public static void main(String[] args) {
		int x=12, y=7;
		x-=y;              // x=x-y
		System.out.println(x);
		
		x+=y;
		System.out.println(x);
		
		/*
		x=y;
		x*=y;
		x/=y;
		x%=y;*/

	}

}
